vim.opt.shiftwidth = 4
vim.opt.expandtab = true -- Convert tabs to spaces
vim.opt.autoindent = true
vim.opt.list = true      -- Show tab characters and trailing whitespace

-- Turn off [No Name] buffer
vim.opt.hidden = false
